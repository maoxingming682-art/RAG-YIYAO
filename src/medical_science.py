def detect_science_topic(question):
    q = str(question or "")
    if any(a in q for a in ANTIBIOTIC_MARKERS) and any(r in q for r in RESPIRATORY_MARKERS):
        return "antibiotic_cold"
    best_key = ""
    best_score = 0
    for key, spec in TOPICS.items():
        score = sum(1 for marker in spec["markers"] if marker in q)
        if score > best_score:
            best_key = key
            best_score = score
    return best_key


def is_science_education_question(question):
    q = str(question or "")
    topic = detect_science_topic(q)
    if any(marker in q for marker in SCIENCE_MARKERS):
        return True
    if topic and any(marker in q for marker in USE_OR_ADVICE_MARKERS):
        return True
    if topic == "antibiotic_cold":
        return True
    return False


def should_use_limited_science_mode(question, max_sim=0.0, evidence_ok=True):
    q = str(question or "")
    topic = detect_science_topic(q)
    if topic == "antibiotic_cold":
        return True
    if not is_science_education_question(q):
        return False
    if max_sim < 0.65 or not evidence_ok:
        return True
    if any(marker in q for marker in ["科普", "为什么", "怎么回事", "原因", "是什么"]):
        return True
    return False


def build_science_reply(question, max_sim=0.0, evidence_reason="", context=None, disclaimer=""):
    q = str(question or "")
    topic = detect_science_topic(q)
    spec = TOPICS.get(topic)
    if not spec:
        lines = [
            "这个问题更适合先按健康科普处理，我可以讲常见原因、风险边界和需要补充的信息。",
            "",
            "但如果涉及具体药品、剂量、疗程、禁忌或同服问题，仍然要回到药品库、说明书或医生/药师建议，不能凭空给结论。",
            "",
            "为了继续判断，请补充：症状持续多久、年龄/特殊人群情况、是否过敏、正在用哪些药。",
        ]
    else:
        lines = [
            spec["opening"],
            "",
            "可以先这样理解：",
        ]
        lines.extend([f"{idx}. {item}" for idx, item in enumerate(spec["points"], 1)])
        lines.extend([
            "",
            "我还需要确认：",
        ])
        lines.extend([f"{idx}. {item}" for idx, item in enumerate(spec["ask"], 1)])
        lines.extend([
            "",
            f"现在可以先做的事：{spec['home']}",
            "",
            f"需要及时就医的情况：{spec['red_flags']}",
        ])

    if evidence_reason:
        lines.extend(["", f"另外，当前药品库证据不足以直接支持具体用药结论：{evidence_reason}。"])

    lines.extend([
        "",
        "如果你接下来问的是具体药品名称、用法用量、不良反应或禁忌，我会再按药品知识库来查。",
    ])
    if disclaimer:
        lines.append(disclaimer)
    return "\n".join(lines)
